// 配置
const config = {
  ollamaUrl: 'http://localhost:11434/api/generate',
  model: 'qwen3.5',
  batchSize: 10,
  delay: 1000 // ms
};

// 存储到数据库的函数
async function saveToDatabase(foodName, recipe) {
  try {
    const response = await axios.post('http://localhost:3000/api/ai/generate-recipe', {
      foodName: foodName
    }, {
      headers: {
        'Content-Type': 'application/json'
      }
    });
    
    console.log(`✅ ${foodName} 菜谱已保存到数据库`);
    return true;
  } catch (error) {
    console.error(`❌ ${foodName} 菜谱保存失败:`, error.message);
    return false;
  }
}

// 使用ollama生成菜谱
async function generateRecipe(foodName) {
  try {
    const response = await axios.post(config.ollamaUrl, {
      model: config.model,
      prompt: `请提供${foodName}的详细制作方法，包含材料、步骤、小贴士，markdown格式`,
      stream: false
    });
    
    return response.data.response;
  } catch (error) {
    console.error(`❌ 生成${foodName}菜谱失败:`, error.message);
    return null;
  }
}

// 批量处理食物
async function processFoods() {
  console.log(`开始为${foodList.length}种食物生成菜谱...`);
  
  let successCount = 0;
  let failCount = 0;
  
  // 分批处理
  for (let i = 0; i < foodList.length; i += config.batchSize) {
    const batch = foodList.slice(i, i + config.batchSize);
    
    console.log(`\n处理第${Math.floor(i / config.batchSize) + 1}批，共${batch.length}种食物`);
    
    // 并行处理批次中的食物
    const promises = batch.map(async (foodName) => {
      try {
        console.log(`正在处理: ${foodName}`);
        
        // 生成菜谱
        const recipe = await generateRecipe(foodName);
        if (!recipe) {
          failCount++;
          return;
        }
        
        // 打印菜谱内容
        console.log(`\n=== ${foodName} 菜谱内容 ===`);
        console.log(recipe);
        console.log(`=======================\n`);
        
        // 保存到数据库
        const saved = await saveToDatabase(foodName);
        if (saved) {
          successCount++;
        } else {
          failCount++;
        }
        
        // 添加延迟避免请求过快
        await new Promise(resolve => setTimeout(resolve, config.delay));
        
      } catch (error) {
        console.error(`❌ ${foodName} 处理失败:`, error.message);
        failCount++;
      }
    });
    
    // 等待批次完成
    await Promise.all(promises);
    
    console.log(`批次完成: 成功${successCount}, 失败${failCount}`);
  }
  
  console.log(`\n处理完成！`);
  console.log(`总食物数: ${foodList.length}`);
  console.log(`成功: ${successCount}`);
  console.log(`失败: ${failCount}`);
}

// 运行脚本
processFoods().catch(error => {
  console.error('脚本执行失败:', error);
  process.exit(1);
});